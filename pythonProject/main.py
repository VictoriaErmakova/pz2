class SystemBlock:
    def __init__(self, proccesor, blockpit, matplata):
        self.proccesor = proccesor
        self.blockpit = blockpit
        self.matplata = matplata
def get_proccesor(self):
    return self.proccesor
def set_proccesor(self, proccesor):
    self.proccesor = proccesor
def get_blockpit(self):
    return self.blockpit
def set_blockpit(self, blockpit):
    self.blockpit = blockpit
def get_matplata(self):
    return self.matplata
def set_matplata(self, matplata):
    self.matplata = matplata
